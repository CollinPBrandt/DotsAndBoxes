import java.util.Random;

public class Box extends AbstractBoardElement{

    /////////////////////////////////////////////////////////////
    //Fields
    /////////////////////////////////////////////////////////////

    public int value;
    public boolean complete;

    /////////////////////////////////////////////////////////////
    //Constructor
    /////////////////////////////////////////////////////////////

    public Box(int row, int column) {
        super(row, column);
        this.complete = false;
        this.value = new Random().nextInt(6);
    }

    /////////////////////////////////////////////////////////////
    //Methods
    /////////////////////////////////////////////////////////////

    @Override
    public void print() {
        System.out.printf(" %d", value);
    }
}


