package Board;

public abstract class AbstractBoardElement {

    /////////////////////////////////////////////////////////////
    //Fields
    /////////////////////////////////////////////////////////////

    public int row;
    public int column;

    /////////////////////////////////////////////////////////////
    //Constructor
    /////////////////////////////////////////////////////////////

    public AbstractBoardElement(int row, int column) {
        this.row = row;
        this.column = column;
    }

    /////////////////////////////////////////////////////////////
    //Abstract Methods
    /////////////////////////////////////////////////////////////

    public abstract void print();
}
